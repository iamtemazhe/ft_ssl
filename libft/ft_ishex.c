#include "libft.h"

t_bool		ft_ishex(int c)
{
	if (('0' <= c && c <= '9') ||\
		('a' <= c && c <= 'f') || ('A' <= c && c <= 'F'))
		return (TRUE);
	return (FALSE);
}
