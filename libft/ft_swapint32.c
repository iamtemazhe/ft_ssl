#include <stdint.h>

uint32_t		ft_swapint32(uint32_t x)
{
	return (((x & 0x000000FF) << 24) |\
			((x & 0x0000FF00) << 8) | \
			((x & 0x00FF0000) >> 8) |\
			((x & 0xFF000000) >> 24));
}
