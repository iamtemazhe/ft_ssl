#include <unistd.h>

int		ft_putchari(int c)
{
	char	ds[4];

	if (c < 0x80)
		return (write(1, &c, 1));
	else if (c < 0x00000800)
	{
		ds[1] = 0x80 | (c & 0x3F);
		ds[0] = 0xC0 | ((c >> 6) & 0x1F);
		return (write(1, ds, 2));
	}
	else
	{
		ds[2] = 0x80 | (c & 0x3F);
		ds[1] = 0x80 | ((c >> 6) & 0x3F);
		ds[0] = 0xE0 | ((c >> 12) & 0xF);
		return (write(1, ds, 3));
	}
}
