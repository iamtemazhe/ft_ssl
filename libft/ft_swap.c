#include <unistd.h>

void		ft_swap(int *a, int *b)
{
	int		c;

	c = *a;
	*a = *b;
	*b = c;
}

void		ft_lswap(size_t *a, size_t *b)
{
	size_t	c;

	c = *a;
	*a = *b;
	*b = c;
}
